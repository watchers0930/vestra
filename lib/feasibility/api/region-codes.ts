/**
 * 주소 문자열에서 매칭되는 지역명을 추출.
 * 두 매핑(REPS, MOIS) 모두에서 사용 가능한 공통 로직.
 *
 * @param address - 주소 문자열 (예: "서울특별시 강남구 역삼동")
 * @returns 매칭된 지역명 또는 null
 */
export function extractRegionFromAddress(address: string): string | null {
  const normalized = address
    .replace(/특별자치시|특별자치도|특별시|광역시|도$/g, "")
    .replace(/\s+/g, "");

  // ADMIN_CODE_MAP이 더 넓은 범위를 커버하므로 기준으로 사용
  const entries = Object.keys(ADMIN_CODE_MAP)
    .sort((a, b) => b.length - a.length);

  for (const key of entries) {
    if (normalized.includes(key)) return key;
  }
  return null;
}
